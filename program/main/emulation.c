void app_main(void)
{
    uint8_t mac_addr[8] = {0x02, 0x00, 0x00, 0xBE, 0xEE, 0xEF};
    esp_base_mac_addr_set(mac_addr);

    initialize_nvs();

    /* Initialize WiFi */
    initialize_wifi();

    static httpd_handle_t server = NULL;
    /* Start the server for the first time */
    server = start_webserver();


    printf("\n =======================================================\n");
    printf(" |               HTTP SERVER ON 80                     |\n");
    printf(" =======================================================\n\n");

}

